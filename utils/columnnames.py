class ColumnNames:

    def __init__(self,
                 open_str: str = None,
                 high_str: str = None,
                 low_str: str = None,
                 close_str: str = None,
                 adj_close_str: str = None,
                 volume_str: str = None):
        self.open_str = open_str
        self.high_str = high_str
        self.low_str = low_str
        self.close_str = close_str
        self.adj_close_str = adj_close_str
        self.volume_str = volume_str
